#!/usr/bin/env bash
# Record a PR-ready task: appends pr=<url> and a verified pr_head=<sha> to
# state/<id>.meta when available, then arms the watcher's merge poll by writing
# state/<id>.check.sh, which prints one line iff the PR is merged (the watcher's
# check contract: output = wake firstmate, silence = keep sleeping).
# Usage: fm-pr-check.sh <task-id> <pr-url>
set -eu

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
FM_ROOT="${FM_ROOT_OVERRIDE:-$(cd "$SCRIPT_DIR/.." && pwd)}"
FM_HOME="${FM_HOME:-${FM_ROOT_OVERRIDE:-$FM_ROOT}}"
STATE="${FM_STATE_OVERRIDE:-$FM_HOME/state}"
"$FM_ROOT/bin/fm-guard.sh" || true
ID=${1:?usage: fm-pr-check.sh <task-id> <pr-url>}
URL=${2:?usage: fm-pr-check.sh <task-id> <pr-url>}

# Validate the PR URL against a strict GitHub allowlist BEFORE it touches any
# interpolation. The URL later becomes the input to a generated state/<id>.check.sh
# the watcher runs through bash; a URL containing $(...) or "; would otherwise be
# executable code (RCE). Reject anything that is not exactly a GitHub PR URL.
case "$URL" in
  *[!A-Za-z0-9:/._-]*)
    echo "error: PR URL contains disallowed characters: $URL" >&2; exit 1 ;;
esac
if ! [[ "$URL" =~ ^https://github\.com/[A-Za-z0-9._/-]+/pull/[0-9]+$ ]]; then
  echo "error: not a GitHub PR URL (expected https://github.com/<owner>/<repo>/pull/<number>): $URL" >&2
  exit 1
fi

META="$STATE/$ID.meta"
if [ -f "$META" ]; then
  WT=$(grep '^worktree=' "$META" | tail -1 | cut -d= -f2- || true)
  LOCAL_HEAD=
  PR_HEAD=
  if [ -n "$WT" ] && [ -d "$WT" ]; then
    LOCAL_HEAD=$(git -C "$WT" rev-parse --verify HEAD 2>/dev/null || true)
    if [ -n "$LOCAL_HEAD" ] && command -v gh >/dev/null 2>&1; then
      if REMOTE_HEAD=$(cd "$WT" && gh pr view "$URL" --json headRefOid -q .headRefOid 2>/dev/null); then
        if [ "$LOCAL_HEAD" = "$REMOTE_HEAD" ]; then
          PR_HEAD=$LOCAL_HEAD
        fi
      fi
    fi
  fi
  if ! grep -qxF "pr=$URL" "$META"; then
    echo "pr=$URL" >> "$META"
  fi
  if [ -n "$PR_HEAD" ] && ! grep -qxF "pr_head=$PR_HEAD" "$META"; then
    echo "pr_head=$PR_HEAD" >> "$META"
  fi
fi

# Belt-and-suspenders against the heredoc-RCE class: write the (already validated)
# URL to a sibling DATA file and have the generated check read it as data, so the
# URL is never embedded as code in the generated script. Only the firstmate-owned
# state path is interpolated, and it is %q-quoted.
URL_FILE="$STATE/$ID.pr-url"
printf '%s\n' "$URL" > "$URL_FILE"

cat > "$STATE/$ID.check.sh" <<EOF
# Auto-generated by fm-pr-check.sh. Reads the PR URL from a sibling data file
# rather than embedding it, so the URL is treated strictly as data, never code.
# Check contract: print one line iff the PR is merged, stay silent otherwise.
url=\$(cat $(printf '%q' "$URL_FILE") 2>/dev/null) || exit 0
[ -n "\$url" ] || exit 0
state=\$(gh pr view "\$url" --json state -q .state 2>/dev/null)
[ "\$state" = "MERGED" ] && echo "merged"
EOF
echo "armed: state/$ID.check.sh polls $URL"
